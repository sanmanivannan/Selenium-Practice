package JavaBasics;

public class StringConcatenation {

	public static void main(String[] args) {
		
		int a = 100;
		int b = 200;
		
		String x = "Hello";
		String y = "World";
		
		double c = 12.33;
		double d = 10.33;
		
	
		
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(a+b+c+d);
		
				

	}

}
