package JavaCore;

public class DataType {

	public static void main(String[] args) {
		// int
		int i = 10;
		int j = 30;
		int k = -25;
		int l = 0;
		System.out.println(l);

		// double
		double d2 = 1.223;
		double d1 = 23.43;
		double d3 = 100;

		// char (should be single digit value and should be within single quote)

		char a = 'a';
		char a1 = 'A';
		char a2 = '1';

		// boolean

		boolean b1 = true;
		boolean b2 = false;

		// String (String is a class)
		// Should be written in double quotes
		String s1 = "Hello World";
		String s2 = "this is my java code";

		

	}

}
